function Write-ProgressBar {
    param([int]$Percent, [string]$Label)
    $width = 40
    $filled = [math]::Floor($Percent / 100 * $width)
    $empty = $width - $filled
    $bar = ("в–€" * $filled) + ("в–‘" * $empty)
    Write-Host "   $CYв•‘$R  $Label" -NoNewline
    Write-Host " [$CY$bar$R] $Percent%  "
}

